# Write a program to whether a number (accepted from user) is divisible by 2 and 3 both.

num = int(input("enter a num"))

if num%2 == 0 and num%3 == 0 : print("both")
elif num%2 == 0 : print("by 2 only")
elif num%3 == 0 : print("by 3 only") 
else : print("not divided by 2 and 3")


